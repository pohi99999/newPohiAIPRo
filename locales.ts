export const translations: Record<Locale, Record<string, string>> = {
  hu: huTranslations,
  en: enTranslations,
  de: deTranslations,
};

// --- Helper functions for translations ---

type TFunction = (key: TranslationKey, params?: Record<string, string | number>) => string;

export const getTranslatedUserRole = (role: UserRole, t: TFunction): string => {
  const keyMap: Record<UserRole, TranslationKey> = {
    [UserRole.ADMIN]: 'userRole_ADMIN',
    [UserRole.CUSTOMER]: 'userRole_CUSTOMER',
    [UserRole.MANUFACTURER]: 'userRole_MANUFACTURER',
  };
  return t(keyMap[role]);
};

export const getTranslatedDemandStatus = (status: DemandStatus, t: TFunction): string => {
  const keyMap: Record<DemandStatus, TranslationKey> = {
    [DemandStatus.RECEIVED]: 'demandStatus_RECEIVED',
    [DemandStatus.PROCESSING]: 'demandStatus_PROCESSING',
    [DemandStatus.COMPLETED]: 'demandStatus_COMPLETED',
    [DemandStatus.CANCELLED]: 'demandStatus_CANCELLED',
  };
  return t(keyMap[status]);
};

export const getTranslatedStockStatus = (status: StockStatus | undefined, t: TFunction): string => {
  if (!status) return t('status_unknown');
  const keyMap: Record<StockStatus, TranslationKey> = {
    [StockStatus.AVAILABLE]: 'stockStatus_AVAILABLE',
    [StockStatus.RESERVED]: 'stockStatus_RESERVED',
    [StockStatus.SOLD]: 'stockStatus_SOLD',
  };
  return t(keyMap[status]);
};

